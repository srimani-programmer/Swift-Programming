// Switch Statements

import UIKit

var myFavouriteColor : String = "gold"
switch myFavouriteColor {
case "red":
    print("You Got 100 Points.")
case "blue":
    print("You Got 90 Points.")
case "orange":
    print("You Got 80 Points.")
case "silver":
    print("You Got 70 Points.")
case "gold":
    print("You got 60 Points.")
case "pink":
    print("You got 50 Points.")
default:
    print("You Got 40 Points")
}
